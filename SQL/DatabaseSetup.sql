CREATE TABLE Production_Orders (
    OrderID INT PRIMARY KEY,
    OrderDate DATE,
    CustomerName VARCHAR(100),
    ProductType VARCHAR(50),
    Quantity INT,
    MVA_Rating DECIMAL(10,2),
    ScheduledCompletionDate DATE,
    ActualCompletionDate DATE,
    Status VARCHAR(50),
    WorkshopID INT
);

CREATE TABLE Daily_Production (
    ProductionID INT PRIMARY KEY,
    ProductionDate DATE,
    WorkshopID INT,
    ProductType VARCHAR(50),
    UnitsProduced INT,
    UnitsRejected INT,
    PlannedUnits INT,
    ShiftHours DECIMAL(5,2),
    EmployeeCount INT
);

CREATE TABLE Quality_Metrics (
    QualityID INT PRIMARY KEY,
    InspectionDate DATE,
    OrderID INT,
    ProductType VARCHAR(50),
    DefectType VARCHAR(100),
    DefectSeverity VARCHAR(20),
    ReworkRequired BIT,
    ReworkHours DECIMAL(5,2)
);

CREATE TABLE Workshop_Capacity (
    WorkshopID INT PRIMARY KEY,
    WorkshopName VARCHAR(100),
    MaxDailyCapacity INT,
    ProductTypes VARCHAR(200),
    EmployeeCount INT,
    SquareMeters INT
);

CREATE TABLE Production_Bottlenecks (
    BottleneckID INT PRIMARY KEY,
    ReportDate DATE,
    WorkshopID INT,
    BottleneckType VARCHAR(100),
    DelayHours DECIMAL(5,2),
    ImpactedOrders INT,
    ResolutionStatus VARCHAR(50)
);

